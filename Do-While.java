
public class DoWhile {

	public static void main(String[] args) {
		int a=2;
		while(a<5)
		{
			System.out.println("The value of a is "+a);
			a++;
		}
		// in do while it will first execute the code and checks the condition 
		int b=0;
      do {
    	  System.out.println(b);
    	  b++;
      }while(b<5);
	}

}
